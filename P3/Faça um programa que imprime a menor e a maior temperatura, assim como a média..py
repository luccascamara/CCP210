temperaturas = [-10, -8, 0, 1, 2, 5, -2, -4]

menor_temp = min(temperaturas)
maior_temp = max(temperaturas)
media_temp = sum(temperaturas) / len(temperaturas)

print(f"A menor temperatura é {menor_temp}°C")
print(f"A maior temperatura é {maior_temp}°C")
print(f"A temperatura média é {media_temp}°C")
